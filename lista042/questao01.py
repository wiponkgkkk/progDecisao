'''
1. Fazer um algoritmo que peça um número, e ao final, informe se o número digitado está acima de 1000 ou
abaixo de 1000.
'''

# Inputs

num = int(input("Informe um número: "))

# Condições e Prints

if num >= 1000:
    print("O número informado está acima de 1000")
elif num < 1000:
    print("O número informado está acima de 1000")