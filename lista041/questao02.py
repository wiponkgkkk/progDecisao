'''
2) Desenvolver um programa que permita ao aluno responder qual a capital do Brasil. O programa deverá exibir se
a resposta está certa ou errada.
'''

# Inputs

cap = input("Qual a capital do Brasil: ")

# Condições e Prints

if (cap == "Brasília"):
    print("Parabéns a resposta esta certa")
else:
    print("Infelizmente a resposta esta errada")